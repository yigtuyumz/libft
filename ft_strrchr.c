char	*ft_strrchr(const char *s, int c)
{
	char	*d;
	char	*ret;

	ret = 0;
	d = (char *) s;
	while (*d != 0)
	{
		if (*d == c)
			ret = d;
		d++;
	}
	if (c == 0)
		return (d);
	return (ret);
}
